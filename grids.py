class SudokuGrid:
    id = 1
    instance_list = []

    def __init__(self, grid):
        self.grid = grid
        self.id = SudokuGrid.id
        SudokuGrid.id += 1
        SudokuGrid.instance_list.append(self)

    def __str__(self):
        return "SudokuGrid " + str(self.id)

    def get_grid(id):
        for grid in SudokuGrid.instance_list:
            if grid.id == id:
                print("Found grid with id " + str(id))
                return grid.grid

